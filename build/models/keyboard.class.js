/**
 * Class representing the state of the keyboard inputs.
 */
class Keyboard {
    /**
     * Represents the state of the LEFT key.
     * @type {boolean}
     */
    LEFT;

    /**
     * Represents the state of the RIGHT key.
     * @type {boolean}
     */
    RIGHT;

    /**
     * Represents the state of the UP key.
     * @type {boolean}
     */
    UP;

    /**
     * Represents the state of the DOWN key.
     * @type {boolean}
     */
    DOWN;

    /**
     * Represents the state of the SPACE key.
     * @type {boolean}
     */
    SPACE;

    /**
     * Represents the state of the D key (for throwing bottles).
     * @type {boolean}
     */
    D;
}
